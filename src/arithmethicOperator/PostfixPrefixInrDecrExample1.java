package arithmethicOperator;

public class PostfixPrefixInrDecrExample1 {
    public static void main(String[] args) {
        int a = 0;
        int b = 5;
        // постфикс инкремент
        a++;
        System.out.println(a);
        // постфикс декремент
        b--;
        System.out.println(b);
        // префикс инкремент
        ++a;
        System.out.println(a);
        // префикс декремент
        --b;
        System.out.println(b);
    }
}
